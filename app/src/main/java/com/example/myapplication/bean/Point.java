package com.example.myapplication.bean;

public class Point {
    public int x;
    public int y;
    public int color;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Point(int x, int y, int color) {
        this.x = x;
        this.y = y;
        this.color = color;
    }
}
